<b>Thinking about how to promote your brand on Instagram?</b></br> Well, if you were the one telling customers that your product
 was the best, why would they believe you? But if an authoritative figure says your product is awesome, consumers are
 more likely to trust them. Agree? Influencer marketing it is!
In case some of you aren’t aware yet, digital influencers are people who have cultivated a social media presence; 
they have followers in the thousands, sometimes even millions, and their pictures and videos get massively liked,
 shared, and commented on. Due to their popularity and loyal fanbase, these folks have become trusted thought 
 leaders and thanks to the wide audience that already loves and trusts them, they can make any brand famous
 within varied different publics. That’s why you must count them as one of the key actors of your brand development in 2018!
According to the latest statistics, more than 60% of marketers increased their revenue in 2017 by using highly 
targeted influencers, and over a third of marketers spent up to $5,000 on influencer marketing. 
For 2018 a recent survey predicts that around 40 percent of companies will increase influencer marketing and social 
media budgets, with the majority planning to spend between $25,000 and $50,000 throughout the year. It’s interesting that 92%
 of respondents named Instagram as the most important network for influencer marketing followed by Facebook at 77 percent.</br></br></br>
<img src="../images/influ5.jpg" ></br></br></br>
A watch brand Daniel Wellington is one of the first companies that fully understood the potential of cooperating
 with influencers. With thousands of posts with their hashtag and nearly 4 million followers on Instagram, 
 they make the perfect example simply to show the scale of impact that influencers can generate. 
The brand pays celebrities and gifts watches to lesser-known micro-influencers to post photos of the watch.
 Simple as this. Efficient as hell. I was delighted to be a part of their massive campaign on Instagram, among the other celebrities,
 bloggers, and influential personalities over there. The brand absolutely trusted us with the quality and creativity of the 
 pictures we were posting on our accounts. And because we were all kinda creative communicators, we made their Instagram 
 feed positively breath-taking. Instagram was increasingly flooded by super-cool, smart and artistic captures of the watch,
 which was exactly what draw you in to want to know more about the brand. A huge buzz was created around the brand, and 
 #DanielWellington became iconic among Millennials from all around the World, exposed massively to those posts.</br></br></br>
